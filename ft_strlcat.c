#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	dest_len;
	size_t	src_len;

	i = 0;
	j = 0;
	dest_len = ft_strlen(dest);
	src_len = ft_strlen(src);
	if (size < dest_len)
		return (size + src_len);
	while (dest[i])
		i++;
	while (src[j] && j + dest_len + 1 < size)
		dest[i++] = src[j++];
	dest[i] = '\0';
	if (dest_len < size)
		return (dest_len + src_len);
	return (size + src_len);
}
