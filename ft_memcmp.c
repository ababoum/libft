#include "libft.h"

int	ft_memcmp(const void *ptr1, const void *ptr2, size_t size)
{
	size_t				i;
	const unsigned char	*tmp1;
	const unsigned char	*tmp2;

	tmp1 = ptr1;
	tmp2 = ptr2;
	i = 0;
	while (i < size)
	{
		if (tmp1[i] != tmp2[i])
			return (tmp1[i] - tmp2[i]);
		i++;
	}
	return (0);
}
